$(document).ready(function() {
  if ($('#touchsurface2').length){
    $.getJSON('/users', null, function(data) {
      items = data;
      minipic = data[1].picture_url.mini.url;
      console.log(items);
      console.log(minipic);

      for (var i = 0; i < items.length; i++) {
        minipic = items[i].picture_url.mini.url;
        itemname = items[i].name;
        var menu = $('<a class="menu-block"><img src='+minipic+'/><span class="menu-icon"></span>'+itemname+'</a>');
        var select =$(".menu");
        menu.appendTo(select);
      }
    });
  }
});
